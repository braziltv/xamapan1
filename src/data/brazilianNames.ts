// Lista completa de todos os nomes (sem duplicatas)
export const ALL_NAMES = [
  ...new Set([
    ...BRAZILIAN_NAMES.masculinos,
    ...BRAZILIAN_NAMES.femininos,
    ...BRAZILIAN_NAMES.regionaisMG.masculinos,
    ...BRAZILIAN_NAMES.regionaisMG.femininas
  ])
].sort((a, b) => a.localeCompare(b, 'pt-BR'));

// Lista completa de sobrenomes
export const ALL_SURNAMES = [
  ...new Set([
    ...BRAZILIAN_NAMES.sobrenomes,
    ...BRAZILIAN_NAMES.regionaisMG.sobrenomes
  ])
].sort((a, b) => a.localeCompare(b, 'pt-BR'));

// Função para buscar nomes que começam com o texto digitado
export function searchNames(query: string, limit = 10): string[] {
  if (!query || query.length < 1) return [];
  
  const normalizedQuery = query.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  
  // Busca nomes que começam com a query (prioridade)
  const startsWithMatches = ALL_NAMES.filter(name => {
    const normalizedName = name.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    return normalizedName.startsWith(normalizedQuery);
  });
  
  // Busca nomes que contêm a query (secundário)
  const containsMatches = ALL_NAMES.filter(name => {
    const normalizedName = name.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    return !normalizedName.startsWith(normalizedQuery) && normalizedName.includes(normalizedQuery);
  });
  
  return [...startsWithMatches, ...containsMatches].slice(0, limit);
}

// Função para buscar sobrenomes
export function searchSurnames(query: string, limit = 10): string[] {
  if (!query || query.length < 1) return [];
  
  const normalizedQuery = query.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  
  const startsWithMatches = ALL_SURNAMES.filter(name => {
    const normalizedName = name.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    return normalizedName.startsWith(normalizedQuery);
  });
  
  const containsMatches = ALL_SURNAMES.filter(name => {
    const normalizedName = name.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    return !normalizedName.startsWith(normalizedQuery) && normalizedName.includes(normalizedQuery);
  });
  
  return [...startsWithMatches, ...containsMatches].slice(0, limit);
}

// Função para buscar em todo o dicionário (nomes + sobrenomes)
export function searchAll(query: string, limit = 10): { names: string[], surnames: string[] } {
  return {
    names: searchNames(query, limit),
    surnames: searchSurnames(query, limit)
  };
}
